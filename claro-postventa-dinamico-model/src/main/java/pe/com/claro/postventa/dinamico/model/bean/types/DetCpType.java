package pe.com.claro.postventa.dinamico.model.bean.types;

public class DetCpType {

	private String codSolot;
	private String idDet;
	private String observacion;
	private String tipoEquipProv;
	private Integer cantTotal;
	private Integer cantEfec;
	private String tipSrv;
	private String idEstado;
	private String codAction;
	
	public String getCodSolot() {
		return codSolot;
	}
	public void setCodSolot(String codSolot) {
		this.codSolot = codSolot;
	}
	public String getIdDet() {
		return idDet;
	}
	public void setIdDet(String idDet) {
		this.idDet = idDet;
	}
	public String getObservacion() {
		return observacion;
	}
	public void setObservacion(String observacion) {
		this.observacion = observacion;
	}
	public String getTipoEquipProv() {
		return tipoEquipProv;
	}
	public void setTipoEquipProv(String tipoEquipProv) {
		this.tipoEquipProv = tipoEquipProv;
	}
	public Integer getCantTotal() {
		return cantTotal;
	}
	public void setCantTotal(Integer cantTotal) {
		this.cantTotal = cantTotal;
	}
	public Integer getCantEfec() {
		return cantEfec;
	}
	public void setCantEfec(Integer cantEfec) {
		this.cantEfec = cantEfec;
	}
	public String getTipSrv() {
		return tipSrv;
	}
	public void setTipSrv(String tipSrv) {
		this.tipSrv = tipSrv;
	}
	public String getIdEstado() {
		return idEstado;
	}
	public void setIdEstado(String idEstado) {
		this.idEstado = idEstado;
	}
	public String getCodAction() {
		return codAction;
	}
	public void setCodAction(String codAction) {
		this.codAction = codAction;
	}
	
	
}
