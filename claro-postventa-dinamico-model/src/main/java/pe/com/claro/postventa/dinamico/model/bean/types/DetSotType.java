package pe.com.claro.postventa.dinamico.model.bean.types;

public class DetSotType {

	private String codsolot;
	private String servicio;
	private String tipsrv;
	private String tipo;
	private String detalle;
	
	public String getCodsolot() {
		return codsolot;
	}
	public void setCodsolot(String codsolot) {
		this.codsolot = codsolot;
	}
	public String getServicio() {
		return servicio;
	}
	public void setServicio(String servicio) {
		this.servicio = servicio;
	}
	public String getTipsrv() {
		return tipsrv;
	}
	public void setTipsrv(String tipsrv) {
		this.tipsrv = tipsrv;
	}
	public String getTipo() {
		return tipo;
	}
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	public String getDetalle() {
		return detalle;
	}
	public void setDetalle(String detalle) {
		this.detalle = detalle;
	}
}
