package pe.com.claro.postventa.dinamico.canonical;

public class TransformaValorRequest {
	private int idSeq;
	private String valor;
	/**
	 * @return the idSeq
	 */
	public int getIdSeq() {
		return idSeq;
	}
	/**
	 * @param idSeq the idSeq to set
	 */
	public void setIdSeq(int idSeq) {
		this.idSeq = idSeq;
	}
	/**
	 * @return the valor
	 */
	public String getValor() {
		return valor;
	}
	/**
	 * @param valor the valor to set
	 */
	public void setValor(String valor) {
		this.valor = valor;
	}
	
}
