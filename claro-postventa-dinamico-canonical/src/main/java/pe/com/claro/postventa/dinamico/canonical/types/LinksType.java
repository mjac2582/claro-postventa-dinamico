package pe.com.claro.postventa.dinamico.canonical.types;

public class LinksType {
	
	private String rel;
	
	private String href;

	public String getRel() {
		return rel;
	}

	public void setRel(String rel) {
		this.rel = rel;
	}

	public String getHref() {
		return href;
	}

	public void setHref(String href) {
		this.href = href;
	}

	
}
