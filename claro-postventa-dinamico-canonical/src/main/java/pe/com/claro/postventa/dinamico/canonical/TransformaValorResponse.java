package pe.com.claro.postventa.dinamico.canonical;

public class TransformaValorResponse {
	private String valorTransformado;
	private int codResp;
	private String msjResp;
	/**
	 * @return the valorTransformado
	 */
	public String getValorTransformado() {
		return valorTransformado;
	}
	/**
	 * @param valorTransformado the valorTransformado to set
	 */
	public void setValorTransformado(String valorTransformado) {
		this.valorTransformado = valorTransformado;
	}
	/**
	 * @return the codResp
	 */
	public int getCodResp() {
		return codResp;
	}
	/**
	 * @param codResp the codResp to set
	 */
	public void setCodResp(int codResp) {
		this.codResp = codResp;
	}
	/**
	 * @return the msjResp
	 */
	public String getMsjResp() {
		return msjResp;
	}
	/**
	 * @param msjResp the msjResp to set
	 */
	public void setMsjResp(String msjResp) {
		this.msjResp = msjResp;
	}
	
}
